export function getAnalysisLanguage(): AnalysisLanguage {
  const storedLanguage = localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return storedLanguage === "zh" ? "zh" : "en";
}

export function setAnalysisLanguage(language: AnalysisLanguage): void {
  localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
}

export function t(key: string, replacements: Record<string, string | number> = {}): string {
  const language = getAnalysisLanguage();
  const template = dictionaries[language][key] ?? dictionaries.en[key] ?? key;

  return Object.entries(replacements).reduce(
    (text, [name, value]) => text.replaceAll(`{{${name}}}`, String(value)),
    template,
  );
}

export function optionLabel(value: string): string {
  return t(`option.${value}`);
}

export function isAnalysisLanguage(value: string): value is AnalysisLanguage {
  return value === "en" || value === "zh";
}
